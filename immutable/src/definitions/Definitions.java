package definitions;
/**
 * switch between two possible modes of the client behaviour:
 * 
 * when 
 * <code>SEND_IMMUTABLE = true</code>
 * the client sends an Immutable object ( <code>ImmutableImpl</code> )
 * 
 * when 
 * <code>SEND_IMMUTABLE = false</code>
 * the client sends a NOT Immutable object ( <code>NotImmutableImpl</code> )
 * 
 * @author	Lorenzo Rotteglia	
 * 
 */
public final class Definitions {
	public static final boolean SEND_IMMUTABLE = true; 

}
